#ifndef _MYIIC_H
#define _MYIIC_H
#include "sys.h"


// SCL  ------>  PB8   
// SDA  ------>  PB9
//IO方向设置
#define SDA_IN()  {GPIOB->MODER&=~(3<<(9*2));GPIOB->MODER|=0<<9*2;}	//PB9输入模式
#define SDA_OUT() {GPIOB->MODER&=~(3<<(9*2));GPIOB->MODER|=1<<9*2;} //PB9输出模式

//IO操作
#define IIC_SCL   PBout(8) //SCL
#define IIC_SDA   PBout(9) //SDA
#define READ_SDA  PBin(9)  //输入SDA

//IIC所有操作函数
void SoftSim_IIC_Init(void);                //初始化IIC的IO口				 
void SoftSim_IIC_Start(void);				//发送IIC开始信号
void SoftSim_IIC_Stop(void);	  			//发送IIC停止信号
void SoftSim_IIC_Send_Byte(u8 txd);			//IIC发送一个字节
u8 SoftSim_IIC_Read_Byte(unsigned char ack);//IIC读取一个字节
u8 SoftSim_IIC_Wait_Ack(void); 				//IIC等待ACK信号
void SoftSim_IIC_Ack(void);					//IIC发送ACK信号
void SoftSim_IIC_NAck(void);				//IIC不发送ACK信号

void SoftSim_IIC_Write_One_Byte(u8 daddr,u8 addr,u8 data);
u8 SoftSim_IIC_Read_One_Byte(u8 daddr,u8 addr);	 


#endif

